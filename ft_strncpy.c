#include "libft.h"

char	*ft_strncpy(char *d, char *s, unsigned int n)
{
	unsigned int i;

	i = 0;
	while (s[i] != '\0' && i < n)
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
